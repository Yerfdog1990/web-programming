// 1. Equal Operator ==
console.log(5 == "5"); // true -> compares two values and allows type conversion

// 2. Strict Equal Operator ===
console.log(5 === "5"); // false -> compares two values but does not allow type conversion

// 3. Not Equal Operator !=
console.log(5 != "5"); // false -> compares two values and allows type conversion

// 4. Strict Not Equal Operator !==
console.log(5 !== "5"); // true -> compares two values but does not allow type conversion

// 5. Greater Than Operator >
console.log(5 > 2); // true

// 6. Less Than Operator <
console.log(5 < 2); // false

// 7. Greater Than or Equal Operator >=
console.log(5 >= 2); // true

// 8. Less Than or Equal Operator <=
console.log(5 <= 2); // false

// 9. Logical AND Operator &&
console.log(true && true); // true
console.log(false && false); // false
console.log(true && false); // false
console.log(false && true); // false
